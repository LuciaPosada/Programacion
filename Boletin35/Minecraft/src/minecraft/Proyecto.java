
package minecraft;

public class Proyecto {
    
    private String nombre;
    
    private int madera;
    private int piedra;
    private int hierro;
    private int carbon;
    private int arena;
    private int arcilla;
    private int oro;
    private int diamante;
    private int cuero;
    private int cristal;
    private int tierra;

    public Proyecto(String nombre, int madera, int piedra, int hierro, int carbon, int arena, int arcilla, int oro, int diamante, int cuero, int cristal, int tierra) {
        this.nombre = nombre;
        this.madera = madera;
        this.piedra = piedra;
        this.hierro = hierro;
        this.carbon = carbon;
        this.arena = arena;
        this.arcilla = arcilla;
        this.oro = oro;
        this.diamante = diamante;
        this.cuero = cuero;
        this.cristal = cristal;
        this.tierra = tierra;
    }
 
}
