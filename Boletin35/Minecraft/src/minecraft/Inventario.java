
package minecraft;

public class Inventario {
    
    private int madera;
    private int piedra;
    private int hierro;
    private int carbon;
    private int arena;
    private int arcilla;
    private int oro;
    private int diamante;
    private int cuero;
    private int cristal;
    private int tierra;

    public Inventario() {
        this.madera = 0;
        this.piedra = 0;
        this.hierro = 0;
        this.carbon = 0;
        this.arena = 0;
        this.arcilla = 0;
        this.oro = 0;
        this.diamante = 0;
        this.cuero = 0;
        this.cristal = 0;
        this.tierra = 0;
    }

    public int getMadera() {
        return madera;
    }

    public int getPiedra() {
        return piedra;
    }

    public int getHierro() {
        return hierro;
    }

    public int getCarbon() {
        return carbon;
    }

    public int getArena() {
        return arena;
    }

    public int getArcilla() {
        return arcilla;
    }

    public int getOro() {
        return oro;
    }

    public int getDiamante() {
        return diamante;
    }

    public int getCuero() {
        return cuero;
    }

    public int getCristal() {
        return cristal;
    }

    public int getTierra() {
        return tierra;
    }
    

    public void setMadera(int madera) {
        this.madera = madera;
    }

    public void setPiedra(int piedra) {
        this.piedra = piedra;
    }

    public void setHierro(int hierro) {
        this.hierro = hierro;
    }

    public void setCarbon(int carbon) {
        this.carbon = carbon;
    }

    public void setArena(int arena) {
        this.arena = arena;
    }

    public void setArcilla(int arcilla) {
        this.arcilla = arcilla;
    }

    public void setOro(int oro) {
        this.oro = oro;
    }

    public void setDiamante(int diamante) {
        this.diamante = diamante;
    }

    public void setCuero(int cuero) {
        this.cuero = cuero;
    }

    public void setCristal(int cristal) {
        this.cristal = cristal;
    }

    public void setTierra(int tierra) {
        this.tierra = tierra;
    }

}
